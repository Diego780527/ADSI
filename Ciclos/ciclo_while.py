# varias veces

# Ciclo While
# Se usa para repetir un número de veces desconocido

print ("--------------- Números de 1 a 20 ----------------")
# Variable control de ciclo
# que el ciclo no se haga infinito
x = 0 

while x <=20:
    print(x)
    #la variable debe sufrir un cambio ara cerrar el ciclo
    x = x + 1