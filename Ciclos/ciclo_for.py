# Imprmir los números de 0 al 20
#Sintaxis: Se usa la palabra for
# se lee para cada
# El i se le asigna el nomnbre de variable de iteración
# Para cada i en el rango de 0 a 21 haga sin tomar el 21
# donce 0 es el límite inferior y 21 el límite superior

for i in range(0, 21):
    #bloque de instrucciones del for
    #deja de ejecutarse cuando las iteracciones terminen
    print("Números del 0 al 20:", i)